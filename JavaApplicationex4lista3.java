package javaapplicationex4lista3;

public class JavaApplicationex4lista3 {

    public static void main(String[] args) {

        // Declarando uma estrutura de repetição para a leitura dos números de 0 a 1999 //
        for (int i = 1000; i < 2000; i++) {
            // Declarando condição para que apareçam apenas os números que divididos por 11, dão resto 5 //    
            if (i % 11 == 5) {
                // Imprimindo a variável i //      
                System.out.println(i);

            }

        }

    }

}
